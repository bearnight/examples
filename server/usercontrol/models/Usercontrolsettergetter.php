<?php 
class Usercontrol_Model_Usercontrolsettergetter
{
	public $role_id;
	public $role_name;
	
	public function setRoleid($val)
	{
		if(strlen($val) > 0)
		{
			$this->role_id=(int)$val;
			return $this;
		}
	}
	public function getRoleid()
	{
		
		return $this->role_id;
	}
	public function setRoleName($val)
	{
		if(strlen($val)>0)
		{
			$this->role_name=$val;
		}
	}
	public function getRoleName()
	{
		return $this->role_name;
	}
	
} 